    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $release_number = new Release_number();
        $release_number->release_number_name = $request->release_number_name;
        $release_number->release_number_description = $request->release_number_description;
        $release_number->image_id = $request->image;
        if($release_number->save()){
            return response()->json([
                'data'  =>  true,
            ],200);
        }else{
            return response()->json([
                'data'  =>  false,
                'message'   =>  '情報処理の中に、エーラが発生しました。',
            ],400);
        }
    }

